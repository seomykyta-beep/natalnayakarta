    p1 = PLANET_MEANINGS.get(p1_key)
    p2 = PLANET_MEANINGS.get(p2_key)
    aspect = ASPECT_INTERPRETATIONS.get(aspect_ru)
    
    if not (p1 and p2 and aspect):
        return f'–í–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ {p1_key} –∏ {p2_key} –≤ –∞—Å–ø–µ–∫—Ç–µ {aspect_ru}.'
    
    # –í—ã–±–∏—Ä–∞–µ–º –≤–∞—Ä–∏–∞—Ü–∏—é
    import hashlib
    seed = hash((p1_key, p2_key, aspect_ru)) % len(aspect['variations'])
    variation = aspect['variations'][seed]
    
    # –í—ã–±–∏—Ä–∞–µ–º —Ç–µ–º—É –¥–ª—è –∫–∞–∂–¥–æ–π –ø–ª–∞–Ω–µ—Ç—ã
    p1_theme = p1['themes'].get('growth', p1['themes'].get('positive'))
    p2_theme = p2['themes'].get('growth', p2['themes'].get('positive'))
    
    # –°—Ç—Ä–æ–∏–º —Ç–µ–∫—Å—Ç
    text = f"–ö–æ–≥–¥–∞ {p1['desc_short'].lower()} {p1_theme} –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è —Å —Ç–µ–º, —á—Ç–æ –ø—Ä–µ–¥—Å—Ç–∞–≤–ª—è–µ—Ç {p2['desc_short'].lower()} {p2_theme}, "
