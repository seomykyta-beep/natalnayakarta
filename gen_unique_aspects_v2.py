def generate_text(p1_key, p2_key, aspect_ru):
    """–ì–µ–Ω–µ—Ä–∏—Ä—É–µ—Ç —É–Ω–∏–∫–∞–ª—å–Ω—ã–π —Ç–µ–∫—Å—Ç"""
    
    p1_name, p1_desc = PLANET_DATA.get(p1_key, (p1_key, p1_key))
    p2_name, p2_desc = PLANET_DATA.get(p2_key, (p2_key, p2_key))
    aspect_info = ASPECT_DATA.get(aspect_ru, {})
    
    if not aspect_info:
        return f'–í–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ {p1_name} –∏ {p2_name} –≤ –∞—Å–ø–µ–∫—Ç–µ {aspect_ru}.'
    
    # –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π —à–∞–±–ª–æ–Ω –Ω–∞ –æ—Å–Ω–æ–≤–µ —Ö–µ—à–∞ –¥–ª—è –∫–æ–Ω—Å–∏—Å—Ç–µ–Ω—Ç–Ω–æ—Å—Ç–∏
    seed = hash((p1_key, p2_key, aspect_ru)) % len(aspect_info['templates'])
    template = aspect_info['templates'][seed]
    
    # –ó–∞–º–µ–Ω—è–µ–º –ø–µ—Ä–µ–º–µ–Ω–Ω—ã–µ
    text = template.format(p1=p1_desc, p2=p2_desc)
    
    return text

def main():
    with open('/opt/natal_chart/texts.json', 'r', encoding='utf-8') as f:
        data = json.load(f)
    
    planets_list = list(PLANET_DATA.keys())
    aspects_list = list(ASPECT_DATA.keys())
    
    count = 0
    
    # –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –≤—Å–µ –ø–∞—Ä—ã
    for i, p1 in enumerate(planets_list):
        for p2 in planets_list[i+1:]:
            pair_key = f"{p1}_{p2}"
            
            if pair_key not in data['aspects']:
                data['aspects'][pair_key] = {}
            
            for aspect_ru in aspects_list:
                text = generate_text(p1, p2, aspect_ru)
                data['aspects'][pair_key][aspect_ru] = text
                count += 1
    
    # –°–æ—Ö—Ä–∞–Ω—è–µ–º
    with open('/opt/natal_chart/texts.json', 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    
    print(f'‚úÖ –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞–Ω–æ {count} —É–Ω–∏–∫–∞–ª—å–Ω—ã—Ö —Ç–µ–∫—Å—Ç–æ–≤')
    print(f'üìä –ü–∞—Ä –ø–ª–∞–Ω–µ—Ç: {len(data["aspects"])}')

if __name__ == '__main__':
    main()
