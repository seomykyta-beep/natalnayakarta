"""–î–æ–±–∞–≤–ª–µ–Ω–∏–µ —Ç–µ–∫—Å—Ç–æ–≤ –∞—Å–ø–µ–∫—Ç–æ–≤ –¥–ª—è —É–∑–ª–æ–≤ –∏ –õ–∏–ª–∏—Ç"""

import json

with open('/opt/natal_chart/texts.json', 'r', encoding='utf-8') as f:
    data = json.load(f)

print("üîÆ –î–æ–±–∞–≤–ª—è—é —Ç–µ–∫—Å—Ç–æ–≤ –∞—Å–ø–µ–∫—Ç–æ–≤ –¥–ª—è —É–∑–ª–æ–≤ –∏ –õ–∏–ª–∏—Ç...\n")

# –ò—Å–ø–æ–ª—å–∑—É–µ–º —Ä—É—Å—Å–∫–∏–µ –Ω–∞–∑–≤–∞–Ω–∏—è –∞—Å–ø–µ–∫—Ç–æ–≤, –∫–∞–∫ –≤ JSON
aspects_ru = {
    '–°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ': '–°–æ–µ–¥–∏–Ω–µ–Ω–∏–µ –∞–∫—Ç–∏–≤–∏–∑–∏—Ä—É–µ—Ç –∫–∞—Ä–º–∏—á–µ—Å–∫—É—é —ç–Ω–µ—Ä–≥–∏—é, —Å–ª–∏—è–Ω–∏–µ –¥–≤—É—Ö —Å–∏–ª –≤ –µ–¥–∏–Ω—ã–π –ø–æ—Ç–æ–∫.',
    '–°–µ–∫—Å—Ç–∏–ª—å': '–°–µ–∫—Å—Ç–∏–ª—å –æ—Ç–∫—Ä—ã–≤–∞–µ—Ç –≤–æ–∑–º–æ–∂–Ω–æ—Å—Ç—å, –≥–∞—Ä–º–æ–Ω–∏—á–Ω–æ–µ —Å–æ—Ç—Ä—É–¥–Ω–∏—á–µ—Å—Ç–≤–æ —ç–Ω–µ—Ä–≥–∏–π.',
    '–ö–≤–∞–¥—Ä–∞—Ç': '–ö–≤–∞–¥—Ä–∞—Ç —Å–æ–∑–¥–∞—ë—Ç –Ω–∞–ø—Ä—è–∂–µ–Ω–∏–µ, –≤—ã–∑–æ–≤ –¥–ª—è —Ä–æ—Å—Ç–∞ –∏ —Ç—Ä–∞–Ω—Å—Ñ–æ—Ä–º–∞—Ü–∏–∏.',
    '–¢—Ä–∏–≥–æ–Ω': '–¢—Ä–∏–≥–æ–Ω –¥–∞—Ä—É–µ—Ç –≥–∞—Ä–º–æ–Ω–∏—é, –≤—Ä–æ–∂–¥—ë–Ω–Ω—ã–π —Ç–∞–ª–∞–Ω—Ç –≤ —ç—Ç–æ–π —Å—Ñ–µ—Ä–µ.',
    '–û–ø–ø–æ–∑–∏—Ü–∏—è': '–û–ø–ø–æ–∑–∏—Ü–∏—è –ø–æ–∫–∞–∑—ã–≤–∞–µ—Ç –ø–æ–ª—è—Ä–Ω–æ—Å—Ç—å, –Ω–µ–æ–±—Ö–æ–¥–∏–º–æ—Å—Ç—å –∏–Ω—Ç–µ–≥—Ä–∞—Ü–∏–∏ –ø—Ä–æ—Ç–∏–≤–æ–ø–æ–ª–æ–∂–Ω–æ—Å—Ç–µ–π.',
    '–ö–≤–∏–∫–æ–Ω—Å': '–ö–≤–∏–∫–æ–Ω—Å —Ç—Ä–µ–±—É–µ—Ç –ø—Ä–∏—Å–ø–æ—Å–∞–±–ª–∏–≤–∞—Ç—å—Å—è, –≤–∏–¥–µ—Ç—å –Ω—é–∞–Ω—Å—ã –∏ –≥–∏–±–∫–æ—Å—Ç—å.',
}

# –î–æ–±–∞–≤–ª—è–µ–º –¥–ª—è –≤—Å–µ—Ö –ø–∞—Ä, –≤–∫–ª—é—á–∞—é—â–∏—Ö —É–∑–ª—ã –∏ –õ–∏–ª–∏—Ç
planets_to_add = ['North_node', 'South_node', 'Lilith']
all_planets = ['Sun', 'Moon', 'Mercury', 'Venus', 'Mars', 'Jupiter', 'Saturn', 'Uranus', 'Neptune', 'Pluto', 'Lilith', 'North_node', 'South_node']

count = 0

# –î–æ–±–∞–≤–ª—è–µ–º –≤—Å–µ –∫–æ–º–±–∏–Ω–∞—Ü–∏–∏ –ø–∞—Ä, –∫–æ—Ç–æ—Ä—ã–µ —Å–æ–¥–µ—Ä–∂–∞—Ç —É–∑–ª—ã –∏–ª–∏ –õ–∏–ª–∏—Ç
for p1 in all_planets:
    for p2 in all_planets:
        if p1 < p2:  # –ò–∑–±–µ–≥–∞–µ–º –¥—É–±–ª–µ–π
            # –ü—Ä–æ–≤–µ—Ä—è–µ–º, –µ—Å—Ç—å –ª–∏ –≤ –ø–∞—Ä–µ —É–∑–µ–ª –∏–ª–∏ –õ–∏–ª–∏—Ç
            if p1 in planets_to_add or p2 in planets_to_add:
                pair_name = f"{p1}_{p2}"
                
                # –°–æ–∑–¥–∞—ë–º —Å—Ç—Ä—É–∫—Ç—É—Ä—É –¥–ª—è —ç—Ç–æ–π –ø–∞—Ä—ã, –µ—Å–ª–∏ –µ—ë –Ω–µ—Ç
                if pair_name not in data['aspects']:
                    data['aspects'][pair_name] = {}
                
                # –î–æ–±–∞–≤–ª—è–µ–º —Ç–µ–∫—Å—Ç—ã –¥–ª—è –∫–∞–∂–¥–æ–≥–æ —Ç–∏–ø–∞ –∞—Å–ø–µ–∫—Ç–∞
                for aspect_ru, aspect_text in aspects_ru.items():
                    if aspect_ru not in data['aspects'][pair_name]:
                        # –°–æ–∑–¥–∞—ë–º –ø–æ–ª–Ω—ã–π —Ç–µ–∫—Å—Ç —Å –æ–ø–∏—Å–∞–Ω–∏–µ–º –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏—è
                        full_text = f"{aspect_text}\n\n–≠—Ç–∞ –∫–æ–º–±–∏–Ω–∞—Ü–∏—è —Å–æ–∑–¥–∞—ë—Ç —É–Ω–∏–∫–∞–ª—å–Ω–æ–µ –≤–∑–∞–∏–º–æ–¥–µ–π—Å—Ç–≤–∏–µ —ç–Ω–µ—Ä–≥–∏–π {p1.replace('_', ' ')} –∏ {p2.replace('_', ' ')}. –ò–Ω—Ç–µ–≥—Ä–∏—Ä—É–π—Ç–µ —ç—Ç–∏ —Å–∏–ª—ã –æ—Å–æ–∑–Ω–∞–Ω–Ω–æ –¥–ª—è –≤–∞—à–µ–≥–æ –¥—É—Ö–æ–≤–Ω–æ–≥–æ —Ä–∞–∑–≤–∏—Ç–∏—è."
                        data['aspects'][pair_name][aspect_ru] = {
                            'text': full_text
                        }
                        count += 1
                    elif isinstance(data['aspects'][pair_name][aspect_ru], str):
                        # –ï—Å–ª–∏ —ç—Ç–æ —Å—Ç–∞—Ä—ã–π —Ñ–æ—Ä–º–∞—Ç (–ø—Ä–æ—Å—Ç–æ —Å—Ç—Ä–æ–∫–∞), –ø—Ä–µ–æ–±—Ä–∞–∑—É–µ–º –≤ –Ω–æ–≤—ã–π
                        old_text = data['aspects'][pair_name][aspect_ru]
                        data['aspects'][pair_name][aspect_ru] = {
                            'text': old_text
                        }

# –°–æ—Ö—Ä–∞–Ω—è–µ–º
with open('/opt/natal_chart/texts.json', 'w', encoding='utf-8') as f:
    json.dump(data, f, ensure_ascii=False, indent=2)

print(f'‚úÖ –î–æ–±–∞–≤–ª–µ–Ω–æ {count} —Ç–µ–∫—Å—Ç–æ–≤ –∞—Å–ø–µ–∫—Ç–æ–≤ –¥–ª—è —É–∑–ª–æ–≤ –∏ –õ–∏–ª–∏—Ç')
