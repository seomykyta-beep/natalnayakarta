def update_texts():
    if not TEXTS_FILE.exists():
        return

    with open(TEXTS_FILE, "r", encoding="utf-8") as f:
        data = json.load(f)

    if "Mercury" not in data["signs"]: data["signs"]["Mercury"] = {}
    if "Venus" not in data["signs"]: data["signs"]["Venus"] = {}

    for sign, text in mercury_texts.items():
        data["signs"]["Mercury"][sign] = text
        print(f"✅ Обновлено: Меркурий в {sign}")

    for sign, text in venus_texts.items():
        data["signs"]["Venus"][sign] = text
        print(f"✅ Обновлено: Венера в {sign}")

    with open(TEXTS_FILE, "w", encoding="utf-8") as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

if __name__ == "__main__":
    update_texts()


